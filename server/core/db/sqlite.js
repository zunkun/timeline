let fileDir = path.join(process.env.LOCALAPPDATA, './timeline');

try {
	fs.accessSync(fileDir, fs.F_OK);
} catch (e) {
	fs.mkdirSync(fileDir);
}

const storage = path.join(fileDir, 'timeline.sqlite');
const sqlite = new Sequelize({
	dialect: 'sqlite',
	storage
});

module.exports = sqlite;
